def soma(x, y):
    assert isinstance(x, (int, float, complex)), \
        "x precisa ser int, float ou complex"
    assert isinstance(x, (int, float, complex)), \
        "y precisa ser int, float ou complex"
    return x + y


def subtracao(x, y):
    assert isinstance(x, (int, float, complex)), \
        "x precisa ser int, float ou complex"
    assert isinstance(x, (int, float, complex)), \
        "y precisa ser int, float ou complex"
    return x - y
